	<h1>I. Schématisation et fonctionnement (10 pts)</h1>
	<ol>
		<li>En vous aidant de la documentation disponible, faites l’inventaire de l’instrumentation mis en oeuvre dans la régulation. On précisera : (1 pt)</li>
		<ul>
			<li>leur symbole normalisé ;</li>
			<li>leur marque ;</li>
			<li>leur référence ;</li>
			<li>les caractéristiques principales en les chiffrant.</li>
		</ul>	
		<li>Pour chaque transmetteur, préciser s’il est 2,3 ou 4 fils, ainsi que son câblage sur la maquette. (1 pt)</li>
		<li>Pour chaque transmetteur, préciser son principe de fonctionnement. (1 pt)</li>
		<li>Préciser les éléments suivants : (1 pt)</li>
		<ul>
			<li>la grandeur réglée ;</li>
			<li>la grandeur réglante ;</li>
			<li>l’organe de réglage ;</li>
			<li>une grandeur perturbatrice.</li>
		</ul>
		<li>Expliquer le fonctionnement de la maquette. (2 pts)</li>
		<li>Proposer un schéma TI de votre maquette. (2 pts)</li>
		<li>Proposer un schéma fonctionnel de votre maquette. On repérera sur le schéma les éléments et les grandeurs physiques présents sur la maquette. (2 pts)</li>

	</ol>

	<h1>II. Mode manuel (10 pts)</h1>
	<ol>
		<li>Procéder à la mise en marche du système. On amènera la mesure à 50%. (1 pt)</li>
		<li>Donner alors la valeur de la commande. (1 pt)</li>
		<li>Le procédé est-il stable ? Justifiez votre réponse. (1 pt)</li>
		<li>Enregistrer le passage d’un régime transitoire à un régime permanent. Imprimer votre courbe, puis indiquer la frontière entre les deux régimes. (1 pt)</li>
		<li>Relever la caractéristique statique de votre procédé. (1 pt)</li>
		<li>Votre procédé est-il direct ou inverse ? (1 pt)</li>
		<li>Donner la valeur du gain statique pour une mesure de 50%. (1 pt)</li>
		<li>Enregistrer la réponse indicielle du système à une augmentation de la commande de 100%. (1 pt)</li>
		<li>Donner le temps de réponse à ±10%. (1 pt)</li>
		<li>Donner la valeur du premier dépassement. (1 pt)</li>
	</ol>	
	
