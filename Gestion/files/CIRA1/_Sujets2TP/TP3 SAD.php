<h1>I. Préparation</h1>
<ol>
	<li>Placer sur le schéma TI, les mesures x1 et x2 et les commandes y1 et y2.</li>
	<li>Donner et réaliser le câblage pneumatique définie sur le schéma TI ci-dessus.</li>
	<li>Donner et procéder au câblage électrique des deux capteurs sur les entrée 1 et 2 du régulateur.</li>
</ol>

<h1>II. Réglage boucle 1</h1>
<ol>
	<li>Régler votre régulation PID de pression à l'aide de la méthode du régleur.</li>
	<li>Relever la réponse indicielle de votre boucle. Le système devra rester dans un fonctionnement linéaire (pas de saturation des signaux).</li>
	<li>Relever les performances de votre boucle (temps de réponse à &pm; 5%, erreur statique, premier dépassement).</li>
</ol>

