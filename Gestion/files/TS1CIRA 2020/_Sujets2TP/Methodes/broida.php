

<h1>II. Réglage de la boucle</h1>
<ol>
<li>Déterminer le modèle de <font color="yellow">Broïda</font> de votre procédé à l'aide de la fiche d'identification fournie. Fournir l'enregistrement obtenu avec toutes les constructions nécessaires à l'identification, ainsi que la fiche complétée.</li>
<li>Donner l'équation H(p) de votre modèle.</li>
<li>Déterminer les réglages de votre régulateur, ie Xp, Ti et Td. On utilisera la fiche fournie en annexe.</li>
<li>Donner alors la fonction de transfert C(p).</li>
<li>Commande à 50% à t=0, représenter l'allure de la commande Y en réponse à un échelon de mesure de 4% jusqu'à sa saturation.</li> 
</ol>

<h1>III. Performances</h1>
<ol>
<li>Mesurer les performances de votre réglage. Tous les calculs et constructions devront apparaître sur l'enregistrement utilisé. (temps de réponse à &pm;5%, erreur statique et dépassement).</li>
<li>Optimiser votre réglage, puis mesurer les nouvelles performances obtenues.</li>
</ol>

